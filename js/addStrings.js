var addStrings = function (num1, num2) {
    let newNum1 = +(num1)
    let newNum2 = +(num2)
    let sum = newNum1 + newNum2
    return String(sum)
};
console.log(addStrings('123', '122'))
console.log(addStrings("9333852702227987", "85731737104263"))
